function solve(input) {
  if (input === `USA`) {
    console.log(`English`);
  } else if (input === `England`) {
    console.log(`English`);
  } else if (input === `Spain`) {
    console.log(`Spanish`);
  } else if (input === `Argentina`) {
    console.log(`Spanish`);
  } else if (input === `Mexico`) {
    console.log(`Spanish`);
  } else console.log(`unknown`);
}
solve(`Mexico`);
